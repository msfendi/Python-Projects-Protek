def isPythagoras(a, b, c):
    if a**2 + b**2 == c**2 :
        print('True')
    else:
        print('False')

# Soal A
print('a.   a = 3, b = 4, c = 5 -> ', end='')
isPythagoras(3, 4, 5)

# Soal B
print('b.   a = 5, b = 9, c = 12 -> ', end='')
isPythagoras(5, 9, 12)

# Soal C
print('c.   a = 8, b = 6, c = 10 ->  ', end='')
isPythagoras(8, 6, 10)

# Soal D
print('d.   a = 7, b = 8, c = 11 -> ', end='')
isPythagoras(7, 8, 11)